    private void layDanhSachLoaiMonAn()
    {
        dsLoaiMonAn.clear();
        DAO.QuanLyMonAn dao = new DAO.QuanLyMonAn();
        dsLoaiMonAn = dao.layDanhSachLoaiMonAn();
    }
    
    private void hienThiLoaiMonAn()
    {
        DefaultListModel<String> dlm = new DefaultListModel<>();
        DefaultComboBoxModel<String> dcm = new DefaultComboBoxModel<>();
        //listLoaiMonAn.removeAll();
        for(int i = 0; i < dsLoaiMonAn.size(); i++)
        {
            LoaiMonAn x = dsLoaiMonAn.get(i);
            dlm.add(i, (String)x.getTenLoaiMonAn());
            dcm.addElement((String)x.getTenLoaiMonAn());
        }
        listLoaiMonAn.setModel(dlm);
        cboMaLoaiMonAn.setModel(dcm);
    }
    
    private void layDanhSachMonAn()
    {
        dsMonAn.clear();
        DAO.QuanLyMonAn dao = new DAO.QuanLyMonAn();
        dsMonAn = dao.layDanhSachMonAn();
    }
    
    private void hienThiMonAn()
    {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Mã");
        dtm.addColumn("Tên");
        dtm.addColumn("Giá tiền");
        dtm.addColumn("Tồn kho");
        dtm.setNumRows(dsMonAn.size());
        for(int i = 0; i < dsMonAn.size(); i++)
        {
            MonAn x = dsMonAn.get(i);
            dtm.setValueAt(x.getMaMonAn(),i,0);
            dtm.setValueAt(x.getTenMonAn(), i, 1);
            dtm.setValueAt(x.getGiaTien(), i ,2);
            dtm.setValueAt(x.getSoLuong(),i,3);
        }
        this.tblMonAn.setModel(dtm);
    }
    
    private void lamMoi()
    {
        this.cboMaLoaiMonAn.setSelectedIndex(-1);
        this.txtMaMonAn.setText("");
        this.txtTenMonAn.setText("");
        this.txtGiaTien.setText("");
        this.txtSoLuong.setText("");
        this.txtTimKiemMonAn.setText("");
        layDanhSachMonAn();
        hienThiMonAn();
    }
    
