    private void listLoaiMonAnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listLoaiMonAnMouseClicked
        // TODO add your handling code here:
        int n = this.listLoaiMonAn.getSelectedIndex();
        if(n != -1)
        {
            LoaiMonAn x = dsLoaiMonAn.get(n);
            this.txtMaLoai.setText(String.valueOf(x.getMaLoaiMonAn()));
            this.txtTenLoai.setText(x.getTenLoaiMonAn());
        }
    }//GEN-LAST:event_listLoaiMonAnMouseClicked

    private void btnThemLoaiMonAnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLoaiMonAnActionPerformed
        // TODO add your handling code here:
        if(this.txtTenLoai.getText().trim().equals(""))
        {
            thongBao("Vui lòng nhập: Tên loại món ăn!");
        }
        else
        {
            DAO.QuanLyMonAn dao = new DAO.QuanLyMonAn();
            int n = dao.kiemTraLoaiMonAn(this.txtTenLoai.getText().trim());
            if(n == 1)
            {
                thongBao("Tên loại: " + this.txtTenLoai.getText().trim() + " đã tồn tại!");
            }
            else
            {
                int x = dao.themLoaiMonAn(this.txtTenLoai.getText().trim());
                if(x == -1)
                {
                    thongBao("Lỗi thêm loại món ăn!");
                }
                else
                {
                    thongBao("Thêm thành công.");
                    layDanhSachLoaiMonAn();
                    hienThiLoaiMonAn();
                    this.txtMaLoai.setText("");
                    this.txtTenLoai.setText("");
                    this.listLoaiMonAn.clearSelection();
                }
            }
            
        }
    }//GEN-LAST:event_btnThemLoaiMonAnActionPerformed

